    private void gantiPassword(){
        String passLama=String.valueOf(pfPassLama.getPassword());
        String passBaru=String.valueOf(pfPassBaru.getPassword());
        String konfirmPassBaru=String.valueOf(pfKonfirmasiPassBaru.getPassword());
        if(passLama.equals(ModuleDB.password) && passBaru.equals(konfirmPassBaru) && passBaru.length()>=6){
            String sql="UPDATE user SET password='"+passBaru+"' WHERE id_user="+ModuleDB.idUser;
            try{
                Connection con=ModuleDB.connectDB();
                Statement stmt=con.createStatement();
                stmt.executeUpdate(sql);
                showMessageDialog(null,"Password anda berhasil diubah. Silakan masukan password baru anda pada login berikutnya!");
            }
            catch(SQLException e){
                showMessageDialog(null,e.getMessage());
            }
        }
        else if(!passLama.equals(ModuleDB.password))
            showMessageDialog(null,"Maaf, password lama salah!");
        else if(!passBaru.equals(konfirmPassBaru))
            showMessageDialog(null,"Maaf, password baru dan konfirm password baru tidak cocok!");
        else if(passBaru.length()<6)
            showMessageDialog(null,"Password minimal 6 karakter");
    }
    
