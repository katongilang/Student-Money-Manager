    private void pfPassLamaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pfPassLamaKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_DOWN)
            pfPassBaru.requestFocus();
        else if(evt.getKeyCode()==KeyEvent.VK_ENTER)
            gantiPassword();
    }//GEN-LAST:event_pfPassLamaKeyPressed

    private void pfPassBaruKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pfPassBaruKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_UP)
            pfPassLama.requestFocus();
        else if(evt.getKeyCode()==KeyEvent.VK_DOWN)
            pfKonfirmasiPassBaru.requestFocus();
        else if(evt.getKeyCode()==KeyEvent.VK_ENTER)
            gantiPassword();
    }//GEN-LAST:event_pfPassBaruKeyPressed

